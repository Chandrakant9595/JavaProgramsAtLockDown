package navinJavaSession;

public class Day13_FinalKeyword {

	public static void main(String[] args) {
		
		
		//final keyword 
		//declare var to final
		//if declare var as final then we can't change it
		//if declare parent class as final then we can't access the parent class methods in child class
		//if we declare any method as final in parent class, then we can't override this method in child class
 
		final int i = 20;
		System.out.println(i);
	}

}
