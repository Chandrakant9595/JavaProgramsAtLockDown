package navinJavaSession;

public class Day1_DataTypes {

	public static void main(String args[]) {
		
		// primitive data types - boolean, char, byte, short, int, long, float, double
		// non-primitive data types - string, array
		
		// 1.Boolean
		boolean b1 = true;
		boolean b2 = false;
		System.out.println("Boolean b1 value is: " +b1);
		System.out.println("Boolean b2 value is: " +b2);
		
		// 2.Char
		char c = 'A';
		System.out.println("Char value is: " +c);
		
		// 3.byte
		byte b = 2;
		System.out.println("Byte value is: " +b);
		
		// 4.short
		short s = 10;
		System.out.println("Short value is: " +s);
		
		// 5.integer
		int i = 10;
		i = 20;
		System.out.println("Integer value is: " +i);
		
		// 6.long
		long l = 9595455315L;
		System.out.println("Long value is: " +l);
		
		// 7.float
		float f = 314.55f;
		System.out.println("Float value is: " +f);
		
		// 8.Double
		double d = 33.99;
		System.out.println("Double value is: " +d);
		
		
		
		
		
		// 1.String 
		String s1 = "Selenium with Java";
		System.out.println("String s1 value is: " +s1);
		
		String s2 = new String("Selenium");
		System.out.println("String s2 value is: " +s2);
		
		// 2.array
		
		
	}
}
