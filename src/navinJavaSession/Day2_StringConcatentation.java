package navinJavaSession;

public class Day2_StringConcatentation {

	public static void main(String[] args) {
		
		int a = 30;
		int b = 20;
		
		if (a>b) {
			System.out.println("A is greater");
		}else {
			System.out.println("B is greater");
		}
		
		
			
		int c = 30;
		int d = 30;
		
		if (c==d) {
			System.out.println("C and D same");
		}else {
			System.out.println("C and D not same");
		}
		
		
		
		int a1 = 500;
		int b1 = 600;
		int c1 = 400;
		
		if(a1>b1 & a1>c1) {
			System.out.println("A is greater");
		}else if (b1>c1) {
			System.out.println("B is greater");
		}else {
			System.out.println("C is greater");
		}
	}
}
