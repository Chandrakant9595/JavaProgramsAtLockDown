package navinJavaSession;

public class Day3_Loops {

	public static void main(String args[]) {
		
		
		//for loop
		for(int i=1; i<=10; i++) {
			System.out.println("For loop: " +i);
		}
		
		for(int i=40; i>=30; i--) {
			System.out.println("For loop: " +i);
		}
		
		//while loop
		int j=11;
		while(j<=20) {
			System.out.println("While loop: " +j);
			j++;
		}
		
		
		//do while
		int k=21;
		do {
			System.out.println("Do-While loop: " +k);
			k++;
		} while (k<=30);
	}
	
	
}
